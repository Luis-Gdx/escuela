    private void submit() {
        if (dataList.getComponentCount() > 0) {
            dataList.removeAll();
            jScrollPane2.setViewportView(dataList);
        }
        switch ((String) jComboBox1.getSelectedItem()) {
            case "Artistas":
                new SearchByArtist().thread.start();
                break;
            case "Albunes":
                new SearchByAlbum().thread.start();
                break;
            case "Canción":
                new SearchByTrack().thread.start();
                break;
        }
    }

