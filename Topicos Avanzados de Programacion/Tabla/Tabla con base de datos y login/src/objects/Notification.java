package objects;

/**
 *
 * @author Luis G
 */
public class Notification {

    private int id, status, usuarios_id, grupo_id;
    private String mensaje, remitente, destinatario;

    public Notification(int id, int status, int usuarios_id, int grupo_id, String mensaje, String remitente, String destinatario) {
        this.id = id;
        this.status = status;
        this.usuarios_id = usuarios_id;
        this.grupo_id = grupo_id;
        this.mensaje = mensaje;
        this.remitente = remitente;
        this.destinatario = destinatario;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getUsuarios_id() {
        return usuarios_id;
    }

    public void setUsuarios_id(int usuarios_id) {
        this.usuarios_id = usuarios_id;
    }

    public int getGrupo_id() {
        return grupo_id;
    }

    public void setGrupo_id(int grupo_id) {
        this.grupo_id = grupo_id;
    }

    public String getMensaje() {
        return mensaje;
    }

    public void setMensaje(String mensaje) {
        this.mensaje = mensaje;
    }

    public String getRemitente() {
        return remitente;
    }

    public void setRemitente(String remitente) {
        this.remitente = remitente;
    }

    public String getDestinatario() {
        return destinatario;
    }

    public void setDestinatario(String destinatario) {
        this.destinatario = destinatario;
    }

    @Override
    public String toString() {
        return "<html><div style='margin: 10px'>" + remitente + " dice: " + mensaje + "</div></html>";
    }

}
