    ArrayList<Jugador> jugadores;
    Connector conn = new Connector() {
        @Override
        public void then(ResultSet rs) {
            try {
                jugadores.add(new Jugador(rs.getString("nombre"), rs.getInt("tiempo")));
            } catch (SQLException ex) {
                Logger.getLogger(MainMenu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    };

    String query;

    public MainMenu() {
        this.jugadores = new ArrayList();
        initComponents();
        this.setLocationRelativeTo(null);
        query = "SELECT\n"
                + "puntuaciones.nombre,\n"
                + "puntuaciones.tiempo\n"
                + "FROM\n"
                + "puntuaciones\n"
                + "ORDER BY\n"
                + "puntuaciones.tiempo ASC\n"
                + "LIMIT 0, 10";
        conn.getData(query);
        addPuntuaciones();
    }

    private void addPuntuaciones() {
        int i = 1;
        for (Jugador jugador : jugadores) {
            puntuacionesPanel.add(new Label(i + ") " + jugador.getNombre() + ": " + jugador.getTiempo() + "s"));
            i++;
        }
    }

